## Ejemplo de función en Python
def saludar(nombre):
    mensaje = "Hola, " + nombre + "!"
    return mensaje

print(saludar("nombre"))



# Definición de una función que suma dos números
def suma(a, b):
    return a + b

# Llamada a la función con argumentos
resultado = suma(5, 7)
print(resultado)  # Salida: 12